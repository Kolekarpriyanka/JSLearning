console.log("Swapping using Two Values");
var sweety = "Sweety";
var cutie = "Cutie";
console.log('Before Swap:-','sweety:-',sweety, 'cutie:-',cutie);
var temp = sweety;
sweety=cutie;
cutie=temp;
console.log('After Swap:-','sweety:-',sweety, 'cutie:-',cutie);

